<template>
    <div :class='"tab " + (tabHandler.animated ? "animated" : "")  + " " + (selected ? "selected" : "")' :style='animation'>
        <icon-arrow v-if='tabHandler.arrow' class='arrow' :primary='selected'></icon-arrow>
        <div class='main-container' @click='onClick()'>
            <div class='decorator decorator-top'></div>
            <div class='decorator decorator-middle'>
                <div class='icon'>
                    <i v-if='tabHandler.icon' :class='tabHandler.icon'></i>
                </div>
                <span class='label'>{{ tabHandler.label }}</span>
                <div v-if='selected' class='slot'><slot></slot></div>
            </div>
            <div class='decorator decorator-bottom'></div>
        </div>
    </div>
</template>

<script lang='ts' scoped>
import { Vue, Options } from 'vue-class-component';
import { Prop, Watch } from 'vue-property-decorator';
import IconArrow from '../icon-arrow/icon-arrow.vue';

export interface TabHandler {
    label: string,
    icon: string | null,
    animated: boolean,
    arrow: boolean,
    onChange: Function
}

@Options({
    components: {
        IconArrow
    }
})
export default class Tab extends Vue {
    @Prop()
    selfKey!: string;

    @Prop()
    groupKey!: string;

    @Prop()
    tabHandler!: TabHandler;

    @Watch('key')
    @Watch('groupKey')
    onKeyChanged() {
        this.selected = this.selfKey === this.groupKey;
        if (this.selected) {
            this.$el.ownerDocument.getElementById('audioClick')?.play();
        }
    }

    public selected = false;
    public animation = '';

    public mounted(): void {
        this.onKeyChanged();
        if (this.tabHandler.animated) {
            new Promise(resolve => { setTimeout(() => { resolve(null); }, 100); })
                .then(() => {
                    this.animation = 'opacity: 1; transform: translateX(0);';
                });
        }
        this.addSoundEffects();
    }

    public unmounted(): void {
        this.removeSoundEffects();
    }

    public addSoundEffects(): void {
        this.$el.addEventListener('mouseenter', this.playSoundEffectScroll);
        this.$el.addEventListener('mouseleave', this.stopSoundEffectScroll);
    }

    private removeSoundEffects(): void {
        this.$el.removeEventListener('mouseenter', this.playSoundEffectScroll);
        this.$el.removeEventListener('mouseleave', this.stopSoundEffectScroll);
    }

    private playSoundEffectScroll(): void {
        if (!this.selected) {
            this.$el.ownerDocument.getElementById('audioScroll')?.play();
        }
    }

    private stopSoundEffectScroll(): void {
        const audio = this.$el.ownerDocument.getElementById('audioScroll');
        if (!audio) return;
        audio.pause();
        audio.currentTime = 0;
    }

    public onClick(): void {
        this.tabHandler.onChange(this.selfKey);
    }

}
</script>

