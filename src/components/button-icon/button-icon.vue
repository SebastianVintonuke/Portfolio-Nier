import { Vue } from 'vue-class-component';
import { Prop } from 'vue-property-decorator';

interface ButtonIconHandler {
    icon: string,
    onClick: Function
}

export default class ButtonIcon extends Vue {
    @Prop()
    handler!: ButtonIconHandler;

    public mounted() {
