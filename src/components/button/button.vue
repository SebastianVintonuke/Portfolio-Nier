<template>
<div class='button' @click='buttonHandler.onClick()'>
    <div class='icon'>
        <i v-if:='buttonHandler.icon' :class='buttonHandler.icon'></i>
    </div>
    <span class='label'>{{ buttonHandler.label }}</span>
</div>
</template>

<script lang='ts' scoped>
import { Vue } from 'vue-class-component';
import { Prop } from 'vue-property-decorator';

export interface ButtonHandler {
    label: string,
    icon: string | null,
    onClick: Function
}

export default class Button extends Vue {
    @Prop()
    buttonHandler!: ButtonHandler;

    public mounted(): void {
        this.addSoundEffects();
    }

    public unmounted(): void {
        this.removeSoundEffects();
    }

    public addSoundEffects(): void {
        this.$el.addEventListener('mouseenter', this.playSoundEffectHover);
        this.$el.addEventListener('mouseleave', this.stopSoundEffectHover);
        this.$el.addEventListener('click', this.playSoundEffectClick);
    }

    private removeSoundEffects(): void {
        this.$el.removeEventListener('mouseenter', this.playSoundEffectHover);
        this.$el.removeEventListener('mouseleave', this.stopSoundEffectHover);
        this.$el.removeEventListener('click', this.playSoundEffectClick);
    }

    private playSoundEffectClick(): void {
        this.$el.ownerDocument.getElementById('audioClick')?.play();
    }

    private playSoundEffectHover(): void {
        this.$el.ownerDocument.getElementById('audioScroll')?.play();
    }

    private stopSoundEffectHover(): void {
        const audio = this.$el.ownerDocument.getElementById('audioScroll');
        if (!audio) return;
        audio.pause();
        audio.currentTime = 0;
    }
}
</script>

